#include <iostream>
using namespace std;
int main()
{
    int n;
    cin >> n;

    switch (n)
    {
    case 1:
        cout << "1" << endl;

    case 2:
        cout << "2" << endl;

    default:
        cout << "default" << endl;

    case 3:
        cout << "3" << endl;

    case 4:
        cout << "4" << endl;
    }

    return 0;
}
